export function defaultManifest(
  name: string,
  kind: SDKModuleKind,
  template: SDKGeneratorTemplate = "generic-tool",
): SDKModuleManifest {
  const id = name.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "");
  return {
    id,
    name: name.replace(/-/g, " ").replace(/\b\w/g, (c) => c.toUpperCase()),
    version: "1.0.0",
    description: `OmniMind ${kind}: ${name}`,
    author: "OmniMind Developer",
    kind,
    template,
    route: `/${id}`,
    toolId: id,
    capabilities: TEMPLATE_CAPABILITIES[template],
    permissions: ["network", "filesystem"],
    dependencies: [],
    designSystem: true,
    autoRegister: true,
    minOmniVersion: "12.0.0",
  };
}

function sdkEntryContent(manifest: SDKModuleManifest): string {
  return `/**
 * ${manifest.name} — OmniMind SDK Module
 * Auto-registers with Brain, Memory, Plugins, Marketplace, Design System.
 */
import { getOmniMindSDK } from "@omnimind/sdk/browser";

const sdk = getOmniMindSDK();

export async function boot${manifest.name.replace(/\s/g, "")}() {
  const result = await sdk.register({
    ...manifest,
  });
  console.log("[${manifest.id}] registered:", result.targets);
  return result;
}

export const manifest = ${JSON.stringify(manifest, null, 2)} as const;

export function use${manifest.name.replace(/\s/g, "")}API() {
  return sdk.api;
}
`;
}

function pageContent(manifest: SDKModuleManifest): string {
  return `"use client";

import { DSWorkspaceHeader, DSCard } from "@/design-system";
import { use${manifest.name.replace(/\s/g, "")}API, boot${manifest.name.replace(/\s/g, "")} } from "../sdk/entry";
import { useEffect } from "react";

export default function ${manifest.name.replace(/\s/g, "")}Page() {
  const api = use${manifest.name.replace(/\s/g, "")}API();

  useEffect(() => {
    void boot${manifest.name.replace(/\s/g, "")}();
  }, []);

  return (
    <div className="flex h-full flex-col bg-[color:var(--omni-ds-bg-canvas)]">
      <DSWorkspaceHeader title="${manifest.name}" subtitle="${manifest.description}" />
      <DSCard className="m-4">
        <p className="text-[11px] text-[color:var(--omni-ds-text-primary)]">
          ${manifest.name} is connected to OmniMind Brain, Memory, and Plugin Framework.
        </p>
      </DSCard>
    </div>
  );
}
`;
}

