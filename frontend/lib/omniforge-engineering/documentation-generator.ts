import type { ArchitectAnalysis } from "../omniforge-architect-api";
import type { ArchitectBlueprint, TechStackSelection, WizardState } from "./types";
import { labelForProjectType } from "./wizard-config";

export function blueprintFromAnalysis(
  analysis: ArchitectAnalysis,
  stack: TechStackSelection,
  projectName: string,
): ArchitectBlueprint {
  return {
    architecture: `${analysis.domain_label} · ${analysis.target_stack} stack with ${stack.frontend} + ${stack.backend}`,
    folderStructure: analysis.folder_tree.length
      ? analysis.folder_tree
      : ["src/", "src/components/", "src/pages/", "backend/", "tests/", "docs/"],
    databaseSchema: `Primary DB: ${stack.database} (${analysis.database.reason})`,
    apiPlan: `REST API at ${analysis.routing.api_prefix} · Frontend routes: ${analysis.routing.frontend}`,
    authPlan: `${analysis.auth.strategy} · Providers: ${analysis.auth.providers.join(", ") || "email"} · JWT: ${analysis.auth.jwt}`,
    deploymentPlan: `Deploy to ${stack.deployment} with Docker-ready artifacts`,
    securityPlan: "Env secrets, HTTPS, input validation, rate limiting, OWASP headers",
    testingPlan: "Unit tests (core logic), integration tests (API), smoke tests (CI)",
    performancePlan: "Code splitting, lazy routes, DB indexes, CDN for static assets",
  };
}

export function generateReadme(wizard: WizardState, blueprint: ArchitectBlueprint | null): string {
  const typeLabel = wizard.projectType ? labelForProjectType(wizard.projectType) : "Application";
  return `# ${wizard.projectName || "OmniForge Project"}

> ${typeLabel} — generated by OmniForge Engineering Suite

## Description

${wizard.description || "AI-generated production scaffold."}

## Tech Stack

| Layer | Choice |
|-------|--------|
| Frontend | ${wizard.stack.frontend} |
| Backend | ${wizard.stack.backend} |
| Database | ${wizard.stack.database} |
| ORM | ${wizard.stack.orm} |
| Deploy | ${wizard.stack.deployment} |

## Architecture

${blueprint?.architecture ?? "See docs/ARCHITECTURE.md"}

## Quick Start

\`\`\`bash
# Install dependencies
npm install

# Start development
npm run dev
\`\`\`

## Environment

Copy \`.env.example\` to \`.env\` and fill required variables.

## License

MIT
`;
}

