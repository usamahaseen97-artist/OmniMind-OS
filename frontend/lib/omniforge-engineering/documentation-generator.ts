export function generateApiDoc(analysis: ArchitectAnalysis | null): string {
  const prefix = analysis?.routing.api_prefix ?? "/api/v1";
  return `# API Documentation

Base URL: \`${prefix}\`

## Endpoints

| Method | Path | Description |
|--------|------|-------------|
| GET | ${prefix}/health | Health check |
| POST | ${prefix}/auth/login | Authenticate user |
| GET | ${prefix}/users | List users |
| POST | ${prefix}/users | Create user |

> Auto-generated from architect plan — extend as routes are implemented.
`;
}

export function generateDeploymentGuide(stack: TechStackSelection): string {
  return `# Deployment Guide

Target platform: **${stack.deployment}**

## Steps

1. Build production assets: \`npm run build\`
2. Set environment variables (see ENV.md)
3. Deploy via ${stack.deployment} CLI or dashboard
4. Run database migrations
5. Verify health endpoint

## Docker

\`\`\`bash
docker build -t app .
docker run -p 3000:3000 --env-file .env app
\`\`\`
`;
}

export function generateEnvGuide(analysis: ArchitectAnalysis | null): string {
  const bindings = analysis?.env_bindings ?? [
    { key: "DATABASE_URL", scope: "backend" },
    { key: "JWT_SECRET", scope: "backend" },
    { key: "NEXT_PUBLIC_API_URL", scope: "frontend" },
  ];
  return `# Environment Variables

| Variable | Scope | Description |
|----------|-------|-------------|
${bindings.map((b) => `| \`${b.key}\` | ${b.scope} | Required for ${b.scope} |`).join("\n")}
`;
}

export function generateDatabaseGuide(stack: TechStackSelection, blueprint: ArchitectBlueprint | null): string {
  return `# Database Guide

Engine: **${stack.database}** · ORM: **${stack.orm}**

${blueprint?.databaseSchema ?? ""}

## Migrations

Run migrations after schema changes. Keep seeds idempotent for CI.
`;
}

export type GeneratedDocSet = {
  path: string;
  content: string;
}[];

export function generateAllDocs(
  wizard: WizardState,
  blueprint: ArchitectBlueprint | null,
  analysis: ArchitectAnalysis | null,
): GeneratedDocSet {
  const archDoc =
    blueprint ??
    (analysis
      ? blueprintFromAnalysis(analysis, wizard.stack, wizard.projectName)
      : {
          architecture: `${wizard.projectName} — ${wizard.stack.frontend} + ${wizard.stack.backend}`,
          folderStructure: ["src/", "backend/", "docs/"],
          databaseSchema: `Database: ${wizard.stack.database}`,
          apiPlan: "REST API with versioned routes",
          authPlan: "JWT session-based authentication",
          deploymentPlan: `Deploy to ${wizard.stack.deployment}`,
          securityPlan: "Env secrets, HTTPS, input validation",
          testingPlan: "Unit + integration tests",
          performancePlan: "Lazy loading, caching, CDN",
        });

  return [
    { path: "README.md", content: generateReadme(wizard, archDoc) },
    { path: "docs/ARCHITECTURE.md", content: generateArchitectureDoc(archDoc) },
    { path: "docs/API.md", content: generateApiDoc(analysis) },
    { path: "docs/DEPLOYMENT.md", content: generateDeploymentGuide(wizard.stack) },
    { path: "docs/ENV.md", content: generateEnvGuide(analysis) },
    { path: "docs/DATABASE.md", content: generateDatabaseGuide(wizard.stack, archDoc) },
  ];
}
